export class TranslationService {
  static translateNumber(num: number, language: string = "en"): string {
    const lang = language.toLowerCase();
    
    // Si el idioma no está soportado, usar inglés por defecto
    if (!numberToWords[lang]) {
      return numberToWords["en"][num] || num.toString();
    }
    
    // Si el número está en el diccionario, devolverlo
    if (numberToWords[lang][num]) {
      return numberToWords[lang][num];
    }
    
    // Para números no mapeados, devolver el número como string
    return num.toString();
  }

  static getSupportedLanguages(): string[] {
    return Object.keys(numberToWords);
  }
}
